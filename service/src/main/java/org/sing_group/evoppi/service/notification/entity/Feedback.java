package org.sing_group.evoppi.service.notification.entity;

import static org.sing_group.fluent.checker.Checks.requireEmail;
import static org.sing_group.fluent.checker.Checks.requireNonEmpty;

import java.io.Serializable;

public class Feedback implements Serializable {
  private static final long serialVersionUID = 1L;

  private String email;
  private String subject;
  private String message;

  public Feedback(String email, String subject, String message) {
    this.email = requireEmail(email);
    this.subject = requireNonEmpty(subject);
    this.message = requireNonEmpty(message);
  }

  public String getEmail() {
    return email;
  }

  public String getSubject() {
    return subject;
  }

  public String getMessage() {
    return message;
  }

  @Override
  public int hashCode() {
    final int prime = 31;
    int result = 1;
    result = prime * result + ((email == null) ? 0 : email.hashCode());
    result = prime * result + ((message == null) ? 0 : message.hashCode());
    result = prime * result + ((subject == null) ? 0 : subject.hashCode());
    return result;
  }

  @Override
  public boolean equals(Object obj) {
    if (this == obj)
      return true;
    if (obj == null)
      return false;
    if (getClass() != obj.getClass())
      return false;
    Feedback other = (Feedback) obj;
    if (email == null) {
      if (other.email != null)
        return false;
    } else if (!email.equals(other.email))
      return false;
    if (message == null) {
      if (other.message != null)
        return false;
    } else if (!message.equals(other.message))
      return false;
    if (subject == null) {
      if (other.subject != null)
        return false;
    } else if (!subject.equals(other.subject))
      return false;
    return true;
  }

}
